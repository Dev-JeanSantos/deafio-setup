package com.example.desafiosetup.adapter.output.dynamodb

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGenerateStrategy
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedTimestamp
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable
import com.example.desafiosetup.aplicacao.dominio.modelo.Correntista2

@DynamoDBTable(tableName = "ContaCorrente")
data class CorrentistaModel2(
    @DynamoDBHashKey(attributeName = "pk")
    var pk: String = "",
    @DynamoDBHashKey(attributeName = "sk")
    @DynamoDBAutoGeneratedTimestamp(strategy = DynamoDBAutoGenerateStrategy.CREATE)
    var sk: String? = null,
    @DynamoDBHashKey(attributeName = "nome")
    val nome: String,
    @DynamoDBHashKey(attributeName = "conta")
    val conta: String
) {
    fun toCorrentista2() = Correntista2(
            nome = nome,
            conta = conta
    )
}

fun Correntista2.toCorrentista2() = CorrentistaModel2(
        nome = nome,
        conta = conta
)